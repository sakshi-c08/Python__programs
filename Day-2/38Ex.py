#intersection return common element

myset = {10, 20, 30, 40}  
yorset = {10, 50, 60, 30}  
print(myset.intersection(yorset))  



#difference() method this will return the element 
#present in first set but not in second set

myset = {10, 20, 30, 40}  
yorset = {10, 50, 60, 30} 
print("Output:", myset.difference(yorset))  

